#5. Escape Characters
'''
| Escape Sequence | Meaning      |
| --------------- | ------------ |
| `\n`            | New line     |
| `\t`            | Tab space    |
| `\\`            | Backslash    |
| `\'`            | Single quote |
| `\"`            | Double quote |

'''

print("Line 1\nLine 2")
print("Name \t Jaillani")
print("KM\\Hour")
print("Let\'s Run")
print('Welcome \"Enyatullah\"')